int	ft_ispunct(int c)
{
	if ((c >= 33) && (c <= 47))
		return (1);
	if ((c >= 58) && (c <= 64))
		return (1);
	if ((c >= 91) && (c <= 96))
		return (1);
	if ((c >= 123) && (c <= 126))
		return (1);
	return (0);
}
